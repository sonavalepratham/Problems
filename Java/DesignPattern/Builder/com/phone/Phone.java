package com.phone;

public class Phone{
	private String OS;
	private int ram;
	private String processor;
	private double screenSize;
	private int battery;
	public Phone(String os, int ram, String processor, double screenSize, int battery){
		super();
		this.OS=os;
		this.ram=ram;
		this.processor=processor;
		this.screenSize=screenSize;
		this.battery=battery;
	}
}
